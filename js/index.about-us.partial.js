let myBtn = document.querySelector('.index-about-us__button');

myBtn.addEventListener('mouseover', function(){
    myBtn.style.color = "#ffffff";
    myBtn.style.background = "#e64a17";
  });
  
  myBtn.addEventListener('mouseout', function(){
    myBtn.style.color = "#F7F6F2";
    myBtn.style.background = "#ff5d28";
  });